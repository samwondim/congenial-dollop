export const getRandomQuestion = (edition, level = null) => {
  const ed = editions[edition];
  if (!ed) return null;

  if (edition === "curiosity") {
    const targetLevel = level || Object.keys(ed.levels)[Math.floor(Math.random() * 4)];
    const questions = ed.questions[targetLevel];
    return {
      text: questions[Math.floor(Math.random() * questions.length)],
      edition: "Curiosity Edition",
      level: targetLevel,
      levelName: ed.levels[targetLevel].name,
      color: ed.levels[targetLevel].color
    };
  }

  const questions = ed.questions;
  return {
    text: questions[Math.floor(Math.random() * questions.length)],
    edition: ed.name,
    level: null,
    levelName: null,
    color: null
  };
};

